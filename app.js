// 渲染章节列表
function renderChapterList() {
    const list = document.getElementById('chapter-list');
    list.innerHTML = novel.chapters.map(chapter => 
        `<a href="javascript:void(0)" onclick="openChapter(${chapter.id})" class="chapter-item">${chapter.title}</a>`
    ).join('');
    
    document.getElementById('chapter-count').textContent = novel.chapters.length;
}

// 渲染最新章节
function renderLatestChapter() {
    const latest = novel.chapters[novel.chapters.length - 1];
    const container = document.getElementById('latest-chapter');
    container.innerHTML = `
        <h4>📖 ${latest.title}</h4>
        <p>${latest.content.substring(0, 100)}...</p>
        <a href="javascript:void(0)" onclick="openChapter(${latest.id})" class="btn btn-primary" style="display:inline-block;margin-top:10px;">立即阅读</a>
    `;
}

// 更新统计
function updateStats() {
    document.getElementById('chapter-count').textContent = novel.chapters.length;
}

// 初始化导航
function initNavigation() {
    const navItems = document.querySelectorAll('.nav-item');
    navItems.forEach(item => {
        item.addEventListener('click', function(e) {
            e.preventDefault();
            const target = this.getAttribute('href').substring(1);
            
            navItems.forEach(i => i.classList.remove('active'));
            this.classList.add('active');
            
            document.querySelectorAll('.section').forEach(s => {
                if(s.id !== 'reader') s.classList.add('hidden');
            });
            
            document.getElementById(target).classList.remove('hidden');
        });
    });
}

// 打开章节
function openChapter(id) {
    const chapter = novel.chapters.find(c => c.id === id);
    if(!chapter) return;
    
    currentChapter = id;
    
    document.getElementById('reader-title').textContent = chapter.title;
    document.getElementById('reader-content').innerHTML = chapter.content
        .split('\n')
        .filter(p => p.trim())
        .map(p => `<p>${p}</p>`)
        .join('');
    
    document.querySelectorAll('.section').forEach(s => s.classList.add('hidden'));
    document.getElementById('reader').classList.remove('hidden');
    
    window.scrollTo(0, 0);
}

// 关闭阅读器
function closeReader() {
    document.getElementById('reader').classList.add('hidden');
    document.getElementById('home').classList.remove('hidden');
}

// 上一章
function prevChapter() {
    if(currentChapter > 1) {
        openChapter(currentChapter - 1);
    }
}

// 下一章
function nextChapter() {
    if(currentChapter < novel.chapters.length) {
        openChapter(currentChapter + 1);
    }
}
